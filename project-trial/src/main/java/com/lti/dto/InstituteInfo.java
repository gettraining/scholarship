package com.lti.dto;

public class InstituteInfo {
	
	private long instituteCode;
	private String instituteName;
	private String institutePwd;
	
	public long getInstituteCode() {
		return instituteCode;
	}
	public void setInstituteCode(long instituteCode) {
		this.instituteCode = instituteCode;
	}
	public String getInstituteName() {
		return instituteName;
	}
	public void setInstituteName(String instituteName) {
		this.instituteName = instituteName;
	}
	public String getInstitutePwd() {
		return institutePwd;
	}
	public void setInstitutePwd(String institutePwd) {
		this.institutePwd = institutePwd;
	}
	
	
	
}
